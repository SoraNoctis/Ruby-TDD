class Calculadora
    def soma(val1, val2)
        val1 + val2
    end

    def subtracao(val1, val2)
        val1 - val2
    end

    def multiplicacao(val1, val2)
        val1 * val2
    end

    def divisao(val1, val2)
        val1 / val2
    end

    def elev(val1, val2)
        val1 ** val2
    end

    def raiz(val1)
        Math.sqrt(val1)
    end

end
